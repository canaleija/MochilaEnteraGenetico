package datos;

/**
 *
 * @author Roberto Cruz Leija
 */
public class Item {
    private int peso;
    private int valor;
   
    public Item(int peso, int valor) {
        this.peso = peso;
        this.valor = valor;
    }

    /**
     * @return the peso
     */
    public int getPeso() {
        return peso;
    }

    /**
     * @param peso the peso to set
     */
    public void setPeso(int peso) {
        this.peso = peso;
    }

    /**
     * @return the valor
     */
    public int getValor() {
        return valor;
    }

    /**
     * @param valor the valor to set
     */
    public void setValor(int valor) {
        this.valor = valor;
    }
    
    
    
}
